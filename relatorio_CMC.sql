SELECT F2_FILIAL, F2_NFELETR, F2_DOC,F2_EMISSAO, C5_NUM, C5_EMISSAO, C5_CODCONT, C5_NOMEFAL, C5_NOMECLI, A1_END, C5_XXCEMIT, F2_VALBRUT,F2_SERIE, F2_DESCONT 
FROM SF2010 SF2, SC5010 SC5, SD2010 SD2, SA1010 SA1          
WHERE SF2.D_E_L_E_T_ = '' 
	AND SC5.D_E_L_E_T_ = '' 
	AND SD2.D_E_L_E_T_ = '' 
	AND D2_PEDIDO = C5_NUM 
	AND F2_DOC = D2_DOC 
	AND F2_SERIE = D2_SERIE 
	AND F2_DUPL <> '' 
	AND A1_COD = C5_CLIENTE
	AND F2_EMISSAO BETWEEN '20180101' AND '20180630'
GROUP BY F2_FILIAL, F2_NFELETR, F2_DOC,F2_EMISSAO,C5_NUM, C5_EMISSAO, C5_CODCONT, C5_NOMEFAL, C5_NOMECLI, A1_END, C5_XXCEMIT, F2_VALBRUT,F2_SERIE, F2_DESCONT
ORDER BY F2_DOC  
